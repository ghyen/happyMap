def convert_80(deposit, monthly_rent, rate=0.067):
    """상호전환 최대 80% 기준으로 보증금/월임대료 변환
    - 월임대료의 80%를 보증금으로 전환 (전환율 6.7%)
    - 월임대료 = 원래 월임대료 × 0.2
    - 보증금 = 원래 보증금 + (월임대료 × 0.8 × 12 / 전환율)
    """
    rent_80 = (int(monthly_rent * 0.2) // 100) * 100
    converted_rent = monthly_rent * 0.8
    deposit_increase = round(converted_rent * 12 / rate / 10000) * 10000
    deposit_80 = deposit + deposit_increase
    return deposit_80, rent_80


